import type { ResolvedAgent, ResolvedApp, ResolvedRole, ResolvedTask, ResolvedSkill } from "../resolver/types.js";
import { getAppShortName } from "../generator/toolfilter.js";
import type { RuntimeMaterializer, MaterializationResult, ComposeServiceDef } from "./types.js";

/**
 * Format a role's permitted tools as a readable list.
 * Each line: "  - {appShortName}: tool1, tool2, tool3"
 */
function formatPermittedTools(
  permissions: Record<string, { allow: string[]; deny: string[] }>,
): string {
  const lines: string[] = [];
  for (const [appName, perms] of Object.entries(permissions)) {
    const shortName = getAppShortName(appName);
    lines.push(`  - ${shortName}: ${perms.allow.join(", ")}`);
  }
  return lines.join("\n");
}

/**
 * Find which roles contain a given task (by name).
 */
function findRolesForTask(
  taskName: string,
  roles: ResolvedRole[],
): ResolvedRole[] {
  return roles.filter((role) =>
    role.tasks.some((t) => t.name === taskName),
  );
}

/**
 * Collect all unique skills across all roles.
 */
function collectAllSkills(roles: ResolvedRole[]): Map<string, ResolvedSkill> {
  const skills = new Map<string, ResolvedSkill>();

  for (const role of roles) {
    for (const skill of role.skills) {
      if (!skills.has(skill.name)) {
        skills.set(skill.name, skill);
      }
    }
    for (const task of role.tasks) {
      for (const skill of task.skills) {
        if (!skills.has(skill.name)) {
          skills.set(skill.name, skill);
        }
      }
    }
  }

  return skills;
}

/**
 * Collect all unique tasks across all roles.
 * Returns tuples of [task, owningRoles].
 */
function collectAllTasks(
  roles: ResolvedRole[],
): Array<[ResolvedTask, ResolvedRole[]]> {
  const seen = new Set<string>();
  const result: Array<[ResolvedTask, ResolvedRole[]]> = [];

  for (const role of roles) {
    for (const task of role.tasks) {
      if (!seen.has(task.name)) {
        seen.add(task.name);
        const owningRoles = findRolesForTask(task.name, roles);
        result.push([task, owningRoles]);
      }
    }
  }

  return result;
}

/**
 * Collect all unique apps from a resolved agent's roles.
 */
function collectAllApps(agent: ResolvedAgent): Map<string, ResolvedApp> {
  const apps = new Map<string, ResolvedApp>();
  for (const role of agent.roles) {
    for (const app of role.apps) {
      if (!apps.has(app.name)) {
        apps.set(app.name, app);
      }
    }
  }
  return apps;
}

/**
 * Generate .claude/settings.json content.
 *
 * Creates one MCP server entry per app, keyed by short name.
 * Each entry points to the per-server endpoint exposed by mcp-proxy:
 *   /{shortName}/sse  (SSE)  or  /{shortName}/mcp  (streamable-http)
 */
function generateSettingsJson(
  agent: ResolvedAgent,
  proxyEndpoint: string,
  proxyType: "sse" | "streamable-http",
  proxyToken?: string,
): string {
  const pathSuffix = proxyType === "sse" ? "/sse" : "/mcp";
  const bearerValue = proxyToken
    ? `Bearer ${proxyToken}`
    : "Bearer ${PAM_PROXY_TOKEN}";

  const allApps = collectAllApps(agent);
  const mcpServers: Record<string, object> = {};
  const permissions: string[] = [];

  for (const [appName] of allApps) {
    const shortName = getAppShortName(appName);
    mcpServers[shortName] = {
      type: proxyType,
      url: `${proxyEndpoint}/${shortName}${pathSuffix}`,
      headers: {
        Authorization: bearerValue,
      },
    };
    permissions.push(`mcp__${shortName}__*`);
  }

  const settings = {
    mcpServers,
    permissions: {
      allow: permissions,
      deny: [],
    },
  };

  return JSON.stringify(settings, null, 2);
}

/**
 * Generate a slash command markdown file for a task.
 */
function generateSlashCommand(
  task: ResolvedTask,
  owningRoles: ResolvedRole[],
): string {
  const lines: string[] = [];

  lines.push(`# Generated by pam from ${task.name}@${task.version}`);
  lines.push("");

  for (const role of owningRoles) {
    const roleShortName = getAppShortName(role.name);
    lines.push("## Role Context");
    lines.push(`You are operating as role: ${roleShortName}`);
    lines.push("Permitted tools for this role:");
    lines.push(formatPermittedTools(role.permissions));
    lines.push("Do NOT use tools outside this list even if they appear available.");
    lines.push("");
  }

  if (task.skills.length > 0) {
    lines.push("## Required Skills");
    for (const skill of task.skills) {
      const skillShortName = getAppShortName(skill.name);
      lines.push(`See skills/${skillShortName}/ for ${skill.description}.`);
    }
    lines.push("");
  }

  lines.push("## Task");
  if (task.prompt) {
    lines.push(`[contents of ${task.prompt}]`);
  } else {
    lines.push("[no prompt defined]");
  }

  return lines.join("\n");
}

/**
 * Generate AGENTS.md content.
 */
function generateAgentsMd(agent: ResolvedAgent): string {
  const agentShortName = getAppShortName(agent.name);
  const lines: string[] = [];

  lines.push(`# Agent: ${agentShortName}`);
  lines.push("");
  lines.push("You are an agent managed by pam (Packaged Agent Manager).");
  lines.push("You have multiple roles. Each task you execute specifies which");
  lines.push("role is active. Only use tools permitted by the active role.");
  lines.push("");
  lines.push("## Roles");

  for (const role of agent.roles) {
    const roleShortName = getAppShortName(role.name);
    lines.push("");
    lines.push(`### ${roleShortName}`);
    if (role.description) {
      lines.push(role.description);
    }
    lines.push("");
    lines.push("**Permitted tools:**");
    lines.push(formatPermittedTools(role.permissions));

    if (role.constraints) {
      const hasConstraints =
        role.constraints.maxConcurrentTasks !== undefined ||
        (role.constraints.requireApprovalFor && role.constraints.requireApprovalFor.length > 0);

      if (hasConstraints) {
        lines.push("");
        lines.push("**Constraints:**");
        if (role.constraints.maxConcurrentTasks !== undefined) {
          lines.push(`- Max concurrent tasks: ${role.constraints.maxConcurrentTasks}`);
        }
        if (role.constraints.requireApprovalFor && role.constraints.requireApprovalFor.length > 0) {
          lines.push(`- Requires approval for: ${role.constraints.requireApprovalFor.join(", ")}`);
        }
      }
    }
  }

  return lines.join("\n");
}

/**
 * Generate a skill README.md.
 */
function generateSkillReadme(skill: ResolvedSkill): string {
  const lines: string[] = [];
  const skillShortName = getAppShortName(skill.name);

  lines.push(`# ${skillShortName}`);
  lines.push("");
  lines.push(skill.description);
  lines.push("");
  lines.push("## Artifacts");
  for (const artifact of skill.artifacts) {
    lines.push(`- ${artifact}`);
  }

  return lines.join("\n");
}

/**
 * Claude Code runtime materializer.
 *
 * Generates a workspace directory optimized for the Claude Code CLI:
 * - .claude/settings.json — MCP config pointing to pam-proxy
 * - .claude/commands/*.md — slash commands scoped to roles
 * - AGENTS.md — agent identity and role documentation
 * - skills/{name}/README.md — skill artifact manifests
 */
export const claudeCodeMaterializer: RuntimeMaterializer = {
  name: "claude-code",

  materializeWorkspace(
    agent: ResolvedAgent,
    proxyEndpoint: string,
    proxyToken?: string,
  ): MaterializationResult {
    const result: MaterializationResult = new Map();
    const proxyType = agent.proxy?.type ?? "sse";

    // .claude/settings.json
    result.set(
      ".claude/settings.json",
      generateSettingsJson(agent, proxyEndpoint, proxyType, proxyToken),
    );

    // .claude/commands/{task-short-name}.md
    const allTasks = collectAllTasks(agent.roles);
    for (const [task, owningRoles] of allTasks) {
      const taskShortName = getAppShortName(task.name);
      result.set(
        `.claude/commands/${taskShortName}.md`,
        generateSlashCommand(task, owningRoles),
      );
    }

    // AGENTS.md
    result.set("AGENTS.md", generateAgentsMd(agent));

    // skills/{skill-short-name}/README.md
    const allSkills = collectAllSkills(agent.roles);
    for (const [, skill] of allSkills) {
      const skillShortName = getAppShortName(skill.name);
      result.set(
        `skills/${skillShortName}/README.md`,
        generateSkillReadme(skill),
      );
    }

    return result;
  },

  generateDockerfile(): string {
    return [
      "FROM node:22-slim",
      "",
      "RUN npm install -g @anthropic-ai/claude-code",
      "",
      "ENV DISABLE_AUTOUPDATER=1",
      "",
      "USER node",
      "WORKDIR /home/node/workspace",
      "COPY --chown=node:node workspace/ /home/node/workspace/",
      "",
      'CMD ["claude", "--dangerously-skip-permissions"]',
    ].join("\n");
  },

  generateComposeService(agent: ResolvedAgent): ComposeServiceDef {
    const roleNames = agent.roles
      .map((r) => getAppShortName(r.name))
      .join(",");

    return {
      build: "./claude-code",
      restart: "no",
      volumes: [
        "./claude-code/workspace:/home/node/workspace",
        "./claude-code/.claude:/home/node/.claude",
        "./claude-code/.claude.json:/home/node/.claude.json",
      ],
      working_dir: "/home/node/workspace",
      environment: [
        `PAM_ROLES=${roleNames}`,
      ],
      depends_on: ["mcp-proxy"],
      stdin_open: true,
      tty: true,
      networks: ["agent-net"],
    };
  },

  generateConfigJson(): string {
    return JSON.stringify(
      {
        hasCompletedOnboarding: true,
        projects: {
          "/home/node/workspace": {
            hasTrustDialogAccepted: true,
          },
        },
      },
      null,
      2,
    );
  },
};
