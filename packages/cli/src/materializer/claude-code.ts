import type { ResolvedAgent, ResolvedRole, ResolvedTask } from "@clawmasons/shared";
import { getAppShortName } from "@clawmasons/shared";
import type { RuntimeMaterializer, MaterializationResult, MaterializeOptions } from "./types.js";
import {
  formatPermittedTools,
  collectAllSkills,
  collectAllTasks,
  generateAgentsMd,
  generateSkillReadme,
  ACP_RUNTIME_COMMANDS,
  generateAcpConfigJson,
  generateAgentLaunchJson,
} from "./common.js";

/**
 * Generate .mcp.json content.
 *
 * Creates a single "chapter" MCP server entry pointing at the proxy's
 * unified endpoint:  /sse  (SSE)  or  /mcp  (streamable-http).
 * The proxy handles tool prefixing internally (e.g. github_create_pr).
 */
function generateMcpJson(
  proxyEndpoint: string,
  proxyType: "sse" | "streamable-http",
  proxyToken?: string,
): string {
  const pathSuffix = proxyType === "sse" ? "/sse" : "/mcp";
  const bearerValue = proxyToken
    ? `Bearer ${proxyToken}`
    : "Bearer ${CHAPTER_PROXY_TOKEN}";

  const mcpConfig = {
    mcpServers: {
      chapter: {
        type: proxyType,
        url: `${proxyEndpoint}${pathSuffix}`,
        headers: {
          Authorization: bearerValue,
        },
      },
    },
  };

  return JSON.stringify(mcpConfig, null, 2);
}

/**
 * Generate .claude/settings.json content.
 *
 * Contains only permissions — MCP server config lives in .mcp.json.
 */
function generateSettingsJson(): string {
  const settings = {
    permissions: {
      allow: ["mcp__chapter__*"],
      deny: [],
    },
  };

  return JSON.stringify(settings, null, 2);
}

/**
 * Generate a slash command markdown file for a task.
 */
function generateSlashCommand(
  task: ResolvedTask,
  owningRoles: ResolvedRole[],
): string {
  const lines: string[] = [];

  lines.push(`# Generated by chapter from ${task.name}@${task.version}`);
  lines.push("");

  for (const role of owningRoles) {
    const roleShortName = getAppShortName(role.name);
    lines.push("## Role Context");
    lines.push(`You are operating as role: ${roleShortName}`);
    lines.push("Permitted tools for this role:");
    lines.push(formatPermittedTools(role.permissions));
    lines.push("Do NOT use tools outside this list even if they appear available.");
    lines.push("");
  }

  if (task.skills.length > 0) {
    lines.push("## Required Skills");
    for (const skill of task.skills) {
      const skillShortName = getAppShortName(skill.name);
      lines.push(`See skills/${skillShortName}/ for ${skill.description}.`);
    }
    lines.push("");
  }

  lines.push("## Task");
  if (task.prompt) {
    lines.push(`[contents of ${task.prompt}]`);
  } else {
    lines.push("[no prompt defined]");
  }

  return lines.join("\n");
}

/**
 * Claude Code runtime materializer.
 *
 * Generates a workspace directory optimized for the Claude Code CLI:
 * - .mcp.json — MCP server config pointing to chapter-proxy
 * - .claude/settings.json — permissions (allow/deny)
 * - .claude/commands/*.md — slash commands scoped to roles
 * - AGENTS.md — agent identity and role documentation
 * - skills/{name}/README.md — skill artifact manifests
 */
export const claudeCodeMaterializer: RuntimeMaterializer = {
  name: "claude-code",

  materializeWorkspace(
    agent: ResolvedAgent,
    proxyEndpoint: string,
    proxyToken?: string,
    options?: MaterializeOptions,
  ): MaterializationResult {
    const result: MaterializationResult = new Map();
    const proxyType = agent.proxy?.type ?? "sse";

    // .mcp.json — MCP server config at workspace root
    result.set(
      ".mcp.json",
      generateMcpJson(proxyEndpoint, proxyType, proxyToken),
    );

    // .claude/settings.json — permissions only
    result.set(
      ".claude/settings.json",
      generateSettingsJson(),
    );

    // .claude/commands/{task-short-name}.md
    const allTasks = collectAllTasks(agent.roles);
    for (const [task, owningRoles] of allTasks) {
      const taskShortName = getAppShortName(task.name);
      result.set(
        `.claude/commands/${taskShortName}.md`,
        generateSlashCommand(task, owningRoles),
      );
    }

    // AGENTS.md
    result.set("AGENTS.md", generateAgentsMd(agent));

    // skills/{skill-short-name}/README.md
    const allSkills = collectAllSkills(agent.roles);
    for (const [, skill] of allSkills) {
      const skillShortName = getAppShortName(skill.name);
      result.set(
        `skills/${skillShortName}/README.md`,
        generateSkillReadme(skill),
      );
    }

    // agent-launch.json — tells agent-entry how to bootstrap this agent
    const primaryRuntime = agent.runtimes[0] ?? "claude-code";
    result.set(
      "agent-launch.json",
      generateAgentLaunchJson(primaryRuntime, agent.credentials, options?.acpMode),
    );

    // ACP mode: generate .chapter/acp.json with command
    if (options?.acpMode) {
      const acpCommand = ACP_RUNTIME_COMMANDS[primaryRuntime] ?? primaryRuntime;
      result.set(".chapter/acp.json", generateAcpConfigJson(acpCommand));
    }

    return result;
  },
};
